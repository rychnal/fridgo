// Widen literal string types to string so CS and EN are interchangeable
type Widen<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends (...args: infer A) => string
    ? (...args: A) => string
    : Widen<T[K]>;
};

export type Translations = Widen<typeof translations.cs>;
